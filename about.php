<h1>What is this place?</h1><br />

Welcome to the 90s search engine! I am the artist <a href="https://www.instagram.com/statuaryspaceart/" target="blank"><u>Statuary Space Art</u></a> and 
I have a lot of nostalgia for the old web and the 90s.<br />
The reason why I started this search engine is that I had a longing to browse the internet of the 90s again.<br />
The problem is that I couldn't find any website where it was possible to actually search the old web in a similar way like we can easily do with the modern web on search engines like Google, Yahoo or Bing.<br />
Because I couldn't find this I decided to start the 90s search engine, this search engine enables you to browse through the old web of the 90s and early 2000s. I deliberately choose to limit the age of websites at the most up to 2009, although not many 2008 or 2009 era websites will be included, and if they are, this will mostly be because they haven't changed since their previous years and are the earliest versions left of certain websites. That we can still visit old websites is made possible through the Wayback Machine. (<i>Also consider to send a donation to their organization as this website wouldn't be possible without them.</i>)
<br />
The Wayback Machine has archived billions of webpages since 1996 which is how we are able to visit old webpages. Unfortunately due to the nature of the bots which archive webpages they aren't able to always save all the images, Flash files, movies or any other file on a webpage. 
Despite that I am trying to link to the best working versions of websites and if certain websites are barely browsable they are not included, as to ensure a true browsing experience.<br />
Apart from that this search engine is not merely a mirror for Wayback Machine links. It also includes still active websites which haven't aged since the 90s and 2000s and archived websites from Geocities and other sites which have been archived by lost media activists, I try to make these better findable within this search engine if their content is from before 2009.
<br />
<h1>How to use this search engine</h1>
<br />
There are some ways to make optimal use of this search engine. Despite the Wayback Machine not having saved every music, flash file or movie etc. there are some methods in which you might be able to retrieve certain files.
First of all, as you could find on the main page, many websites back then used Flash. Although Flash is no longer in use, there are emulators with which you can still view Flash files. Not all of them will work, but there are websites with working Flash files archived by the Wayback Machine
which you can view with an add-on for Firefox. <a href="https://addons.mozilla.org/en-US/firefox/addon/flash-player-emulator/?utm_source=addons.mozilla.org&utm_medium=referral&utm_content=search" target="blank">You can download this add-on here.</a>
<br />
<h1>Fixing website</h1><br />
A lot of the links on the Wayback Machine contain broken links or images, or broken Flash files. One of the objectives of this Search Engine is also, wherever possible, to restore websites closely to their original state or to link to files or products which the original websites don't link to anymore. So sometimes you might find a website from the Wayback Machine which has been copied and in which links to font 
or software downloads or "buying" links for 90s games for example are fixed to link to the actual product or file. I will also include an option to still visit the original Wayback Machine links which still link to now dead links for those who'd rather don't be linked to modern websites through it, even though the links are to the products originally advertised.<br />
<h1>Removal of your Website from this Search Engine</h1><br/>
If your website is listed in this search engine and you want it to be removed, for example because it contains personal information about you, please send me a request and I will remove it. I and in the future possible volunteers are adding a lot of websites and we can't check or know for every website that someone mentioned somewhere doesn't want to be found.<br />
<h1>Including your old website</h1><br />
I'd gladly add your website to the search engine, for that at the moment it's best practical if you join the Discord Server linked in the navigation above so that you can link it and give proof, preferably by the Wayback Machine, that it's an actual old website which we can include.
